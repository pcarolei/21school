int	ft_sqrt(int nb)
{
	int res;

	res = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 2147395600)
		return (46340);
	while (res * res < nb && res < 46340)
		res++;
	if (res * res != nb)
		return (0);
	return (res);
}
