#include <unistd.h>

void	ft_generic(void)
{
	int		i;
	char	*str;
	char	nl;

	i = 0;
	str = "Tut tut ; Tut tut";
	nl = '\n';
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, &nl, 1);
}
