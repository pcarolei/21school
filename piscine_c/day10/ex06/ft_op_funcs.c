int	ft_op_plus(int a, int b)
{
	return (a + b);
}

int	ft_op_minus(int a, int b)
{
	return (a - b);
}

int	ft_op_mult(int a, int b)
{
	return (a * b);
}

int	ft_op_div(int a, int b)
{
	return (a / b);
}

int	ft_op_mod(int a, int b)
{
	return (a % b);
}
